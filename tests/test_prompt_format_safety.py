def _expected_yaml_path(template_name: str, locale: str) -> Path:
    """Return where a (template, locale) pair lives on disk.

    English (`en`) lives in the master prompts/ directory; every other locale
    lives under prompts/localized/<lang>/. Returning a Path doesn't validate
    that the file exists — callers handle the missing-locale case explicitly
    (some prompts may not be translated for every locale).
    """
    base = Path(__file__).resolve().parent.parent / "ciris_engine" / "logic" / "dma" / "prompts"
    if locale == "en":
        return base / f"{template_name}.yml"
    return base / "localized" / locale / f"{template_name}.yml"


def _conscience_yaml_path(template_name: str, locale: str) -> Path:
    """Same idea for conscience prompts."""
    base = Path(__file__).resolve().parent.parent / "ciris_engine" / "logic" / "conscience" / "prompts"
    if locale == "en":
        return base / f"{template_name}.yml"
    return base / "localized" / locale / f"{template_name}.yml"


# ──────────────────────────────────────────────────────────────────
# Test 1: safe_format detects leftover placeholders (unit-level)
# ──────────────────────────────────────────────────────────────────


class TestSafeFormat:
    def test_clean_format_returns_rendered(self) -> None:
        """Happy path: all placeholders substituted, returns formatted string."""
        result = safe_format("Hello {name}", source="test", name="world")
        assert result == "Hello world"

    def test_strict_mode_raises_on_leftover(self, monkeypatch: pytest.MonkeyPatch) -> None:
        """Strict mode raises ValueError when leftovers survive .format().

        Use `{{absent}}` (double-brace escape) so .format() doesn't KeyError
        — it's the *post-format* leftover detection we're testing here, not
        the *missing-kwarg* path which Python handles natively.
        """
        monkeypatch.setenv("CIRIS_STRICT_PROMPT_FORMAT", "1")
        with pytest.raises(ValueError, match=r"Unexpanded placeholders.*\['\{absent\}'\]"):
            safe_format("Hello {name}, age {{absent}}", source="test_template", name="world")

    def test_production_mode_warns_on_leftover(self, monkeypatch: pytest.MonkeyPatch, caplog: pytest.LogCaptureFixture) -> None:
        """Production mode (env unset) logs WARNING and returns the malformed string."""
        monkeypatch.delenv("CIRIS_STRICT_PROMPT_FORMAT", raising=False)
        with caplog.at_level("WARNING"):
            # Simulate the cross-package call: format() with extra placeholder
            template = "Hello {name}, age {absent}"
            # `.format(name="world")` would KeyError on `absent`. To exercise
            # the leftover path we use a template where {{absent}} (escaped)
            # becomes literal {absent} in the output:
            result = safe_format("Hello {name}, age {{absent}}", source="prod_test", name="world")
        assert result == "Hello world, age {absent}"
        assert any("Unexpanded placeholders" in rec.message for rec in caplog.records)
        assert any("{absent}" in rec.message for rec in caplog.records)

    def test_json_examples_dont_falsely_match(self) -> None:
        """JSON examples like {"key": "val"} must NOT trip the detector."""
        # JSON example after format() escape-resolution becomes single-brace.
        # The pattern matches identifier-only placeholders, not JSON.
        result = safe_format(
            'Reply as: {{"action": "speak"}}\nUser: {name}',
            source="json_test",
            name="alice",
        )
        assert result == 'Reply as: {"action": "speak"}\nUser: alice'

    def test_action_verb_set_doesnt_match(self) -> None:
        """Lists like {speak, defer, observe} (with commas) must not match the
        identifier-only pattern."""
        # Use {{}} to escape so .format doesn't try to expand
        result = safe_format(
            "Choose from: {{speak, defer, observe}}",
            source="set_test",
        )
        # The output has literal {speak, defer, observe} which contains a comma
        # — the regex requires identifier characters only inside braces.
        leftovers = UNEXPANDED_PLACEHOLDER_PATTERN.findall(result)
        assert leftovers == []


