"""29-language × every-prompt-template harness for unexpanded-placeholder detection.

Why this exists
===============
Prompt templates use Python `.format(**kwargs)` to inject runtime context.
When the template references a variable that the caller doesn't pass — e.g.
because a schema reshape removed the field, or a locale translator left a
stale `{stakeholders}` placeholder, or a YAML author typoed `{user_id}` as
`{userid}` — Python's format() raises KeyError on STRICT missing keys but
silently leaves the literal `{var}` token in the rendered string for the
LLM-permissive paths.

The LLM then receives a malformed prompt with literal placeholder tokens
and produces degraded output with no error signal. This bug class is
historically silent and easy to miss in code review.

What this test does
===================
For each (template_name × locale) combination across 29 locales:

1. Load the prompt via the appropriate loader (DMA or conscience). The
   loader internally calls `safe_format` which DETECTS unexpanded
   placeholders and (in strict mode) RAISES.
2. Format the result with a kitchen-sink kwargs fixture that mirrors what
   the runtime evaluators actually pass.
3. Strict mode is enforced via `CIRIS_STRICT_PROMPT_FORMAT=1`, set by an
   autouse fixture so any leftover `{identifier}` becomes a hard test
   failure.

Runs in parallel via pytest-xdist (29 locales × ~7 templates ≈ 200 cases).

What it catches
===============
- Schema reshapes that drop fields the templates still reference
- YAML translator drift (e.g. ja translator left {user_message} as
  {user_msg} in their copy)
- Missing kwargs at evaluator call sites
- Polyglot block author leaving `{POLYGLOT_*}` markers ungutted
- Stale field names like {stakeholders} from pre-v3.2 PDMA shape
"""
