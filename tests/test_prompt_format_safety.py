# Test 2: 29-locale × every-DMA-template parallel sweep
# ──────────────────────────────────────────────────────────────────


def _gen_dma_param_ids() -> List[Tuple[str, str]]:
    """Generate (locale, template_name) pairs that have an actual YAML on disk."""
    pairs = []
    for locale in ALL_LOCALES:
        for tmpl in DMA_TEMPLATES:
            yaml_path = _expected_yaml_path(tmpl, locale)
            if yaml_path.is_file():
                pairs.append((locale, tmpl))
    return pairs


@pytest.mark.parametrize(
    ("locale", "template_name"),
    _gen_dma_param_ids(),
    ids=lambda v: v,
)
def test_dma_prompt_format_safety(locale: str, template_name: str) -> None:
    """Every DMA template × every locale must format with kitchen-sink kwargs
    and emit zero unexpanded `{identifier}` placeholders.

    Strict mode (autouse fixture) makes leftover placeholders raise ValueError
    out of safe_format inside the loader's get_system_message and
    get_user_message paths.
    """
    loader = DMAPromptLoader(language=locale)
    collection = loader.load_prompt_template(template_name)

    # System message: exercises every formattable system field
    system = loader.get_system_message(collection, **DMA_FORMAT_KWARGS)
    leftovers = sorted(set(UNEXPANDED_PLACEHOLDER_PATTERN.findall(system)))
    assert not leftovers, (
        f"DMA[{locale}/{template_name}] system message has unexpanded "
        f"placeholders after .format(): {leftovers}. Either the template "
        f"references a stale field name, the locale translator left an "
        f"untranslated marker, or DMA_FORMAT_KWARGS in this test is missing "
        f"a kwarg the runtime actually passes."
    )

    # User message: exercises context_integration
    user = loader.get_user_message(collection, **DMA_FORMAT_KWARGS)
    leftovers = sorted(set(UNEXPANDED_PLACEHOLDER_PATTERN.findall(user)))
    assert not leftovers, (
        f"DMA[{locale}/{template_name}] user message has unexpanded "
        f"placeholders after .format(): {leftovers}."
    )


# ──────────────────────────────────────────────────────────────────
# Test 3: YAML-walk lint (catches issues at template-author time)
# ──────────────────────────────────────────────────────────────────


def _resolved_template_placeholders(loader: DMAPromptLoader, template_name: str) -> List[str]:
    """Extract every `{identifier}` placeholder from a template AFTER polyglot
    substitution but BEFORE .format(). Walks every formattable string field on
    the PromptCollection, escapes legitimate JSON-example double-braces (which
    get unescaped only at .format() time), and applies the identifier-only
    pattern.

    Why post-polyglot: the raw YAML contains `{{POLYGLOT_PDMA_FRAMING}}` which
    the regex would (correctly) match the inner `{POLYGLOT_PDMA_FRAMING}` of —
    but that's a load-time substitution marker, not a format-time placeholder.
    Running the check after polyglot expansion eliminates that false positive
    while still surfacing real format-time stale references.

    Why pre-format: we want to validate what the TEMPLATE author intended.
    Post-format would give us the rendered string, but we already have a
    runtime-rendered test for that (test_dma_prompt_format_safety).
    """
    collection = loader.load_prompt_template(template_name)
    placeholders: set[str] = set()
    for field in (
        "system_guidance_header",
        "domain_principles",
        "evaluation_steps",
        "evaluation_criteria",
        "response_format",
        "response_guidance",
        "context_integration",
    ):
        text = getattr(collection, field, None)
        if not text:
            continue
        # Strip {{...}} (legitimate JSON-example escapes) before the identifier
        # match so we don't double-count their inner content.
        sanitized = re.sub(r"\{\{[^{}]*\}\}", "", text)
        placeholders.update(UNEXPANDED_PLACEHOLDER_PATTERN.findall(sanitized))
    return sorted(placeholders)


@pytest.mark.parametrize(
    ("locale", "template_name"),
    _gen_dma_param_ids(),
    ids=lambda v: v,
)
def test_dma_yaml_placeholders_in_known_kwargs(locale: str, template_name: str) -> None:
    """Every `{identifier}` placeholder appearing in any resolved DMA template
    must be in DMA_FORMAT_KWARGS. New placeholders require BOTH the template
    AND the fixture to be updated together — preventing the silent N/A
    regression where a template references a field but the call site doesn't
    supply it.
    """
    loader = DMAPromptLoader(language=locale)
    placeholders = _resolved_template_placeholders(loader, template_name)
    unknown = [p for p in placeholders if p.strip("{}") not in DMA_FORMAT_KWARGS]
    assert not unknown, (
        f"DMA[{locale}/{template_name}] references placeholders not in "
        f"DMA_FORMAT_KWARGS: {unknown}. Either add them to the kitchen-sink "
        f"fixture (and wire them through the evaluator's .format() call) "
        f"OR remove them from the template."
    )
